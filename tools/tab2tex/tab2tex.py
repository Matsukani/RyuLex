def to_tex_entries(entries: List[Entry], profile: OrthoProfile, headletters: bool = True) -> str:
	lines: List[str] = []
	prev_head_key = None
	
	for e in entries:
		if headletters and e.head_key and e.head_key != prev_head_key:
			lines.append(r"\LetterHead{" + e.head_letter + r"}" + "\n")
			prev_head_key = e.head_key

		head_kvs: List[str] = []
		head_kvs.append(keyval_pair("kana", render_ryu(e.kana, profile)))		

		if e.ipa.strip():
			head_kvs.append(keyval_pair("ipa", render_jp_plain(e.ipa)))
		if e.pos.strip():
			head_kvs.append(keyval_pair("pos", render_jp_plain(e.pos)))
		if e.rui.strip():
			head_kvs.append(keyval_pair("rui", render_jp_plain(e.rui)))
		if e.para.strip():
			head_kvs.append(keyval_pair("para", render_ryu(e.para, profile))) # If Paradigm contains only Ryukyuan
		if e.pitch.strip():
			head_kvs.append(keyval_pair("acc", render_jp_plain(e.pitch)))

		head_kvs = [kv for kv in head_kvs if kv]
		head_opt = ",".join(head_kvs)

		meaning_lines: List[str] = []

		for s in e.senses:
			if s.ruby.strip():
				gloss = render_jp_ruby(s.ruby) # applies both ruby rules 
			else:
				gloss = render_jp_prose(s.meaning, profile)

			expl = render_jp_prose(s.comment, profile)

			if s.examples_inline.strip():
				examples = tex_rubyize_general(s.examples_inline) # need to apply to orthoprofile
			else:
				examples = "".join(
					r"\reibun{"
					+ render_ryu(src, profile) 
					+ r"}{"
					+ render_jp_prose(jp, profile) #ruby not properly working?
					+ r"}"
					for (src, jp) in (s.examples_rel or [])
				)

			meaning_lines.append(
				r"\ryumeaning%"
				+ "\n\t\t{" + gloss + r"}{"
				+ expl + r"}{"
				+ examples + r"}%"
			)

		meanings_block = (
			r"\MeaningsBlock{%"
			+ "\n\t"
			+ "\n\t".join(meaning_lines)
			+ "\n\t}%"
		)

		tail_kvs: List[str] = []

		if e.biko.strip():
			biko_tex = render_jp_prose(e.biko, profile)
			tail_kvs.append(keyval_pair("note", biko_tex))	
	
		tail_kvs = [kv for kv in tail_kvs if kv]
		tail_opt = ",".join(tail_kvs)

		entry_tex = (
			r"\entry"
			+ "\n\t{\\headPrint[" + head_opt + r"]}%"
			+ "\n\t{%\n\t\t" + meanings_block + "\n\t}%"
			+ "\n\t{\\tailPrint[" + tail_opt + r"]}%"
			+ "\n"
		)

		lines.append(entry_tex)

	return "".join(lines)

