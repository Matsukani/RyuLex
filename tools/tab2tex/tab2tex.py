from datetime import datetime

# Output filename formatting
def make_outname(excel_path: Path, suffix: str) -> Path:
	stem = excel_path.stem
	date = datetime.now().strftime("%y%m%d")
	return excel_path.with_name(f"{stem}_{suffix}_{date}.tex")

# -------------------
# Command Line Input
# -------------------

def build_argparser() -> argparse.ArgumentParser:
	p = argparse.ArgumentParser(description="Convert Ryukyuan lexicographic data in the RyuTab format (.xlsx) to RyuTeX files.")
	p.add_argument("--settings", default="ryutex-settings.ini", help="Path to settings.ini")
	p.add_argument("-e", "--o-entries", action="store_true", help="Output dictionary entries")
	p.add_argument("-r", "--o-reverse", action="store_true", help="Output Japanese→Ryukyuan reverse index")
	return p

def main() -> int:
	
	# Parse the arguments from command line
	args = build_argparser().parse_args()

	# Error on no output selected
	if not args.o_entries and not args.o_reverse:
		raise SystemExit("Error: specify at least one of --o-entries or --o-reverse")

	# Load settings from the .ini file
	cfg = Settings()
	try:
		cfg, hmap, profile = load_settings(args.settings)
	except Exception as e:
		logging.error(f"Failed to load settings from '{args.settings}': {e}")
		return 2

	# Excel path
	excel_path = cfg.excel_path

	# Read the lexical data sheet from the Excel file
	try:
		df_main = read_sheet(excel_path, cfg.sheet_main)			
	except Exception as e:
		logging.error(f"Failed to read lexical sheet '{cfg.sheet_main}': {e}")
		return 2

	# Read entence example sheet if available
	rei_index = None

	if cfg.sheet_example:
		try:
			df_rei = read_sheet(excel_path, cfg.sheet_example)
		except Exception as e:
			logging.error(f"Failed to read example sheet '{cfg.sheet_example}': {e}")
			return 2

		rei_index = build_rei_index(df_rei, hmap.ex)

	# Build ignorable for sort regex
	ignorable_for_sort = compile_ignorable_for_sort(cfg.ignorable_for_sort)

	# Build entries from the main sheet
	entries = build_entries(
		df_main,
		hmap.main,
		ignorable_for_sort,
		headletter_mode=cfg.headletter_mode,
		rei_index=rei_index
	)

	# Output entries if -e
	if args.o_entries:
		tex_entries = to_tex_entries(entries, profile, cfg.headletters)
		out_entries = make_outname(excel_path, "entries")
		out_entries.write_text(tex_entries, encoding="utf-8")
		print(f"Wrote entries → {out_entries}")

	# Output reverse index if -r
	if args.o_reverse:
		tex_reverse = to_tex_gyaku_jp2ryu(df_main, hmap.main, profile, ignorable_for_sort)
		out_reverse = make_outname(excel_path, "reverse")
		out_reverse.write_text(tex_reverse, encoding="utf-8")
		print(f"Wrote reverse index → {out_reverse}")

	return 0

if __name__ == "__main__":
	raise SystemExit(main())
