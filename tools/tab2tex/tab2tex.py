def require_cols_semantic(df, hmap: Dict[str, str], keys, sheetname: str) -> None:
	missing = []
	for k in keys:
		cname = hmap.get(k, "").strip()
		if cname == "" or cname not in df.columns:
			missing.append(f"{k} -> '{cname}'")
	if missing:
		raise ValueError(f"Missing required columns in {sheetname}: " + ", ".join(missing))

def get_cell_semantic(row, hmap: Dict[str, str], key: str) -> str:
	cname = hmap.get(key, "").strip()
	if cname == "" or cname not in row.index:
		return ""
	return get_cell(row, cname) 

def get_cell(row: pd.Series, col: str) -> str:
	if col not in row.index:
		return ""

	v = row[col]
	
	# Handle pandas NaN / None
	if pd.isna(v):
		return ""
	
	s = str(v)

	return s.strip()

