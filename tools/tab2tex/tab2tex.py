def build_entries(
	df_main: pd.DataFrame,
	hmain: Dict[str, str],
	ignorable_for_sort: Pattern[str],
	headletter_mode: str = "auto",
	rei_index: Optional[Dict[str, List[Tuple[str, str]]]] = None
) -> List[Entry]:

	# Requires at least the ENTRY_ID and KANA_ORTHOGRAPHY columns 
	require_cols_semantic(df_main, hmain, [H_ID, H_KANA], "main")	
	entries: Dict[str, Entry] = {}

	for _, row in df_main.iterrows():
	
		# Skip row If filtered out
		fval = get_cell_semantic(row, hmain, H_FILTER)
		if fval != "":
				continue
		
		# Retrieve Entry ID and Kana orthography
		eid = get_cell_semantic(row, hmain, H_ID)
		kana = get_headword(row, hmain) # kana2 if present else kana
		
		# Skip row with empty EntryID or KANA
		if eid == "" or kana == "": 
			continue

		# Retrieve other values
		kana_sort = gojuon_sort_key(kana, ignorable_for_sort)  # the KANA string for sorting
		head_key = headkey_from_kana(kana, ignorable_for_sort) # normalized grouping key
		head_letter = headletter_from_kana(kana, ignorable_for_sort, headletter_mode) # displayed head letter
		
		ipa = get_cell_semantic(row, hmain, H_IPA)
		pos = get_cell_semantic(row, hmain, H_POS)
		rui = get_cell_semantic(row, hmain, H_RUI)
		para = get_cell_semantic(row, hmain, H_PARA)
		biko = get_cell_semantic(row, hmain, H_BIKO)
		pitch = get_cell_semantic(row, hmain, H_PITCH)

		jun = get_cell_semantic(row, hmain, H_ORDER)
		meaning = get_cell_semantic(row, hmain, H_MEANING)
		ruby = get_cell_semantic(row, hmain, H_RUBY)
		comment = get_cell_semantic(row, hmain, H_COM)


		# ----------------------------
		# Examples policy:
		# 1) If inline reibun exists -> use it verbatim (no splitting)
		# 2) Else if relational index exists -> use that
		# ----------------------------
		
		examples_inline = ""
		examples_rel: List[Tuple[str, str]] = []

		# Inline examples
		inline_rei = get_cell_semantic(row, hmain, H_REI)
		if inline_rei:
			examples_inline = inline_rei

		# Relational sheet
		elif rei_index is not None:
			key = get_cell_semantic(row, hmain, H_MID)
			if key in rei_index:
				examples_rel = rei_index[key]

		sense = Sense(jun=jun, meaning=meaning, ruby=ruby, comment=comment, examples_inline=examples_inline, examples_rel=examples_rel)

		# Add entry if first meaning encountered
		if eid not in entries:
			entries[eid] = Entry(
				entry_id=eid,
				kana=kana,
				kana_sort=kana_sort,
				head_key=head_key,
				head_letter=head_letter,
				ipa=ipa,
				pos=pos,
				rui=rui,
				para=para,
				biko=biko,
				pitch=pitch,
				senses=[sense],
			)
		# Add sense to an already picked up entry
		else:
			entries[eid].senses.append(sense)

	# sort senses by 順 when numeric
	for e in entries.values():
		def _jun_key(s: Sense) -> Tuple[int, str]:
			try:
				return (int(float(s.jun)), s.jun)
			except Exception:
				return (10**9, s.jun)
		e.senses = sorted(e.senses, key=_jun_key)

	return sorted(entries.values(), key=lambda e: (e.kana_sort, e.kana)) # Sorting logic


