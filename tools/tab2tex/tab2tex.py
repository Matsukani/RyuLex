def compile_ignorable_for_sort(chars: str) -> Pattern[str]:
	if not chars:
		return re.compile(r"(?!x)x")
	return re.compile("[" + re.escape(chars) + "]")

def kata_to_hira(text: str) -> str:
	out = []
	for ch in text:
		o = ord(ch)
		if 0x30A1 <= o <= 0x30F6:
			out.append(chr(o - 0x60))
		else:
			out.append(ch)
	return "".join(out)

def clean_kana_core(text: str, ignorable_for_sort: Pattern[str]) -> str:
	if text is None:
		return ""

	# (1) remove whitespace
	text = _WS_ANY.sub("", str(text))

	# (2) remove ignorable characters
	text = ignorable_for_sort.sub("", text)

	if not text:
		return ""

	# (3) normalize small kana -> full size
	text = text.translate(_SMALL_TO_BIG)

	# (4) remove voicing distinctions
	text = "".join(_SEION_MAP.get(ch, ch) for ch in text)

	return text

def prep_kana_for_sort(text: str, ignorable_for_sort: Pattern[str]) -> str:
	# sorting/grouping normalization:
	# core cleanup + script unification
	return kata_to_hira(clean_kana_core(text, ignorable_for_sort))

# Matrix for first-pass Kana sorting order
GOJUON_ROWS = [
	["ー"],
	["あ", "い", "う", "え", "お"],
	["か", "き", "く", "け", "こ"],
	["さ", "し", "す", "せ", "そ"],
	["た", "ち", "つ", "て", "と"],
	["な", "に", "ぬ", "ね", "の"],
	["は", "ひ", "ふ", "へ", "ほ"],
	["ま", "み", "む", "め", "も"],
	["や", "ゆ", "よ"],
	["ら", "り", "る", "れ", "ろ"],
	["わ", "ゐ", "ゑ", "を"],
	["ん"]
]

def gojuon_sort_key(kana: str, ignorable_for_sort: Pattern[str]) -> str:
	kana = prep_kana_for_sort(kana, ignorable_for_sort)

	def _rank(ch: str) -> Tuple[int, int, int]:
		for row_idx, row in enumerate(GOJUON_ROWS, start=1):
			if ch in row:
				return (row_idx, row.index(ch), 0)
		return (99, 99, ord(ch))

	ranks = [_rank(ch) for ch in kana]
	return "".join(f"{r:02d}{c:02d}{f:05d}" for (r, c, f) in ranks)[:240]

def headkey_from_kana(kana: str, ignorable_for_sort: Pattern[str]) -> str:
	k = prep_kana_for_sort(kana, ignorable_for_sort)
	return k[0] if k else ""

def headletter_from_kana(kana: str, ignorable_for_sort: Pattern[str], mode: str = "auto") -> str:
	k = clean_kana_core(kana, ignorable_for_sort)
	if not k:
		return ""

	ch = k[0]

	if mode == "hiragana":
		ch = kata_to_hira(ch)

	return ch

def normalize_head_char(ch: str) -> str:
	# small kana → big
	ch = _SMALL_TO_BIG.get(ch, ch)
	return ch

def get_headword(row, hmain: Dict[str, str]) -> str:
	kana2 = get_cell_semantic(row, hmain, H_KANA2)
	if kana2.strip():
		return kana2
	return get_cell_semantic(row, hmain, H_KANA)

