def to_tex_gyaku_jp2ryu(df_main: pd.DataFrame, hmain: Dict[str, str], profile: OrthoProfile, ignorable_for_sort: Pattern[str]) -> str:
	"""
	Generate Japanese→Ryukyuan reverse index:
	\rentry{JP-yomi}{JP-kanji}{Ryukyuan headwords}

	- Splits multiple JP items by "。"
	- Trims leading head markers like （...）
	- Deduplicates headwords per JP key
	- Sorts by yomi
	"""

	def F_ryu(x: str) -> str:
		return render_ryu(x, profile)

	def F_jp_prose(x: str) -> str:
		return render_jp_prose(x, profile)

	def F_jp_plain(x: str) -> str:
		return render_jp_plain(x)


	def trim_head(s: str) -> str:
		return re.sub(r'（.*?）(?P<a>.*?)', r'\g<a>', str(s))

	def split_items(s: str) -> List[str]:
		s = str(s or "")
		s = s[:-1] if s.endswith("。") else s
		return [p for p in s.split("。") if p != ""]

	# Collect: key = (kanji_trimmed, yomi_trimmed) -> {display_tex: raw_sort_source}
	out: Dict[Tuple[str, str], Dict[str, str]] = {}

	# store also a sort key for yomi
	sortkeys: Dict[Tuple[str, str], str] = {}

	# Require columns
	require_cols_semantic(df_main, hmain, [H_READING, H_MEANING, H_KANA], "main")

	errors: List[str] = []

	for _, row in df_main.iterrows():
		yomi_raw = get_cell_semantic(row, hmain, H_READING)
		kanji_raw = get_cell_semantic(row, hmain, H_MEANING)

		# Skip row then both 意味 and 読み empty
		if yomi_raw == "" and kanji_raw == "":
			continue

		headword = get_headword(row, hmain) # kana2 if present else kana
		if headword == "":
			continue

		# Apply Ryukyuan normalization
		headword_tex = F_ryu(headword)

		# Split JP items
		kanji_items = split_items(kanji_raw) if kanji_raw else []
		yomi_items = split_items(yomi_raw) if yomi_raw else []

		# Strict mirror check
		if len(kanji_items) != len(yomi_items):
			errors.append(
				f"Row {int(row.name)+2}: split count mismatch "
				f"(意味={len(kanji_items)}, 読み={len(yomi_items)}) "
				f"意味='{kanji_raw}' 読み='{yomi_raw}'"
			)
			continue  # skip this row entirely

		for k_item, y_item in zip(kanji_items, yomi_items):
			kanji_trim = trim_head(k_item)
			yomi_trim = trim_head(y_item)

			# derive a stable sort key for yomi
			yomi_sort = gojuon_sort_key(yomi_trim, ignorable_for_sort)

			key = (kanji_trim, yomi_trim)

			# Create bucket if needed
			if key not in out:
				out[key] = {}
				sortkeys[key] = yomi_sort

			# Head treated as Japanese prose with possibility of Ryukyuan embedding
			if re.match(r"^（(?P<a>.*?)）", str(k_item)):
				head = re.sub(r"^（(?P<a>.*?)）.*", r"\g<a>", str(k_item))
				head_tex = "〔" + F_jp_prose(head) + r"\ntilde{}" + "〕"
				headword_tex2 = head_tex + headword_tex
			else:
				headword_tex2 = headword_tex

			# Dedup by exact display TeX, but sort by raw headword only
			out[key][headword_tex2] = headword

	# Sort keys primarily by yomi_sort, then yomi itself
	items = list(out.keys())
	items.sort(key=lambda k: (sortkeys.get(k, ""), prep_kana_for_sort(k[1], ignorable_for_sort), k[1], k[0]))

	# Emit TeX
	lines: List[str] = []
	for (kanji_trim, yomi_trim) in items:
		# Decide whether to show kanji: show only if kanji != yomi
		kanji_out = kanji_trim if (kanji_trim != yomi_trim and kanji_trim != "") else ""

		# Sort displayed headwords by their raw headword source
		heads = list(out[(kanji_trim, yomi_trim)].items())  # [(display_tex, raw_sort_source), ...]
		heads.sort(key=lambda t: (gojuon_sort_key(t[1], ignorable_for_sort), t[1]))

		c = "、".join(display for display, _ in heads)
		lines.append(r"\rentry{" + F_jp_plain(yomi_trim) + r"}{" + F_jp_plain(kanji_out) + r"}{" + c + r"}")

	# Report errors
	if errors:
		raise ValueError("JP↔yomi segmentation errors:\n" + "\n".join(errors))

	return "\n".join(lines) + "\n"

