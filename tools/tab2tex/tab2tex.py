_TEX_SPECIAL = re.compile(r'([%#$&_{}])')
_TEX_TILDE = re.compile(r'~')
_TEX_CARET = re.compile(r'\^')
_TEX_BACKSLASH = re.compile(r'\\')

def tex_escape(s: str) -> str:
	"""
	Conservative TeX escaping.
	Escapes all TeX special characters, including backslash.
	Assumes no raw TeX is present in input data.
	"""
	if s is None:
		return ""
	s = str(s)
	if s == "":
		return ""

	# Escape backslash first
	s = _TEX_BACKSLASH.sub(r'\\textbackslash{}', s)

	s = _TEX_SPECIAL.sub(r'\\\1', s)
	s = _TEX_TILDE.sub(r'\\textasciitilde{}', s)
	s = _TEX_CARET.sub(r'\\textasciicircum{}', s)

	return s

