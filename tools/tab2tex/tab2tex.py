# No case escape + general ruby

def render_ryu(s: str, profile: OrthoProfile) -> str:
	s = tex_escape(s or "")
	return tex_norm_custom(s, profile)

def render_jp_prose(s: str, profile: OrthoProfile) -> str:
	s = tex_escape(s or "")
	s = tex_embify_ryu(s, profile=profile) # Handles embedded Ryukyuan
	s = tex_rubyize_general(s)
	return s

def render_jp_ruby(s: str) -> str:
	s = tex_escape(s or "")
	return tex_rubyize(s) # rubyize already applies general ruby

def render_jp_plain(s: str) -> str:
	return tex_escape(s or "")


# Customizable normalization based on orthographic profile 
def tex_norm_custom(s: str, profile: OrthoProfile) -> str:
	"""
	Project-configurable normalization: apply literal replacements, then regex rules.
	"""
	if s is None:
		return ""
	s = str(s)
	if s == "":
		return ""

	# 1) literal replacements
	for src, dst in profile.replacements.items():
		s = s.replace(src, dst)

	# 2) regex rules
	for rule in profile.rules:
		if isinstance(rule.repl, str):
			s = rule.pat.sub(lambda m, repl=rule.repl: repl, s)
		else:
			s = rule.pat.sub(rule.repl, s)

	for enc in profile.enclosures:
		s = enc.pat.sub(
			lambda m: "\\" + enc.command + "{" + m.group(0) + "}",
			s
		)

	return s

# General pattern for converting ruby
# Allow ruby start at:
#   - BOS
#   - ｜ (consumed)
#   - 、 or 。 (kept)
#   - 、｜ or 。｜ (keep punctuation, consume bar)
_RE_RUBY_GENERAL = re.compile(
	r'(^|｜|、｜|。｜|、|。)'
	r'(?P<a>[^｜〔〕、。]+)'
	r'〔(?P<b>[^〕]+)〕'
)

def tex_rubyize_general(s: str) -> str:
	"""
	Convert strict ruby markup to TeX \\ruby[g]{...}{...}.

	Accepted starts:
	- 語〔ご〕            (BOS only)
	- ｜語〔ご〕          (bar consumed)
	- 、語〔ご〕 / 。語〔ご〕
	- 、｜語〔ご〕 / 。｜語〔ご〕 (punctuation kept; bar consumed)
	"""

	def _repl(m: re.Match) -> str:
		prefix = m.group(1)

		if prefix == "｜":
			prefix = ""
		elif prefix == "、｜":
			prefix = "、"
		elif prefix == "。｜":
			prefix = "。"

		return prefix + r'\ruby[g]{%s}{%s}' % (m.group('a'), m.group('b'))

	return _RE_RUBY_GENERAL.sub(_repl, s)

# Helper functions for converting ruby 

_RUBY_BOUNDARY = "\uE000"
_RE_RUBY = re.compile(
	r'(?P<a>[^。（）' + _RUBY_BOUNDARY + r']+)'  # GREEDY base
	r'（(?P<b>[^）]+)）'
)

# Ruby-specific column
def tex_rubyize(s: str) -> str:
	"""
	Convert in-cell ruby markup to TeX \\ruby[g]{...}{...}.

	Accepted patterns:
	- 語（ルビ）            -> \\ruby[g]{語}{ルビ}
	- 訳語（やくご）        -> \\ruby[g]{訳語}{やくご}
	- 訳（やく）語（ご）    -> \\ruby[g]{訳}{やく}\\ruby[g]{語}{ご}

	Explicit boundary:
	- （） blocks ruby span across the marker
	  引（ひ）っ（）提（さ）げる
	  -> \\ruby[g]{引}{ひ}っ\\ruby[g]{提}{さ}げる

	Multiple meanings / notes:
	- Ruby applies anywhere (not only sentence-initial)
	  （補足）訳語（やくご）。別（べつ）の意味
	  -> （補足）\\ruby[g]{訳語}{やくご}。\\ruby[g]{別}{べつ}の意味

	Rules:
	- Ruby base is greedy and never crosses （ ） 。 or （） boundaries
	- Empty ruby （） is removed after enforcing the boundary
	"""
	
	# Apply general ruby for nested parentheses
	s = tex_rubyize_general(s)
	
	# Convert explicit hard boundary
	s = re.sub(r'（\s*）', _RUBY_BOUNDARY, s)

	# rubyize
	s = _RE_RUBY.sub(r'\\ruby[g]{\g<a>}{\g<b>}', s)

	# remove boundary
	s = s.replace(_RUBY_BOUNDARY, '')

	return s

# Embify
_RE_EMB_RYU = re.compile(r'〈(?P<x>[^〉]*)〉')  # no nesting; deterministic

def tex_embify_ryu(s: str, profile: OrthoProfile) -> str:
	"""
	Convert embedded Ryukyuan marked as 〈...〉 into TeX \\emb{...},
	and apply tex_norm_custom() ONLY to the inside.

	Example:
	「...粟は〈アングル〉...」 -> 「...粟は\\emb{アングル}...」
	(with tex_norm_custom applied inside \\emb{...})
	"""
	if s is None:
		return ""

	def _repl(m: re.Match) -> str:
		inner = m.group("x")
		inner = tex_norm_custom(inner, profile)  # only inside gets project macros
		return r"\emb{" + inner + "}"

	return _RE_EMB_RYU.sub(_repl, s)

def keyval_pair(k: str, v: str) -> str:
	"""
	Build key=val for TeX keyval.
	"""
	v = v.strip()
	if v == "":
		return ""
	
	return f"{k}={{{v}}}"

