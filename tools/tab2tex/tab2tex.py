SPECIAL_REPLACEMENT_KEYS = {
	"ZENKAKU_SPACE": "　",
	"SPACE": " ",
	"TAB": "\t",
	"CARET": "^",
	"LBRACKET": "[",
	"RBRACKET": "]",
}

# Kana orthography classes
@dataclass(frozen=True)
class NormRule:
	pat: Pattern[str]
	repl: str | Callable[[re.Match], str]

@dataclass(frozen=True)
class EnclosureRule:
	pat: Pattern[str]
	command: str

@dataclass(frozen=True)
class OrthoProfile:
	# literal replacements (fast, obvious)
	replacements: Dict[str, str]
	# regex rules (for tone runs, etc.)
	rules: List[NormRule]
	# Enclosure rules
	enclosures: List[EnclosureRule]

def load_ortho_profile(cfg: configparser.ConfigParser, name: str) -> OrthoProfile:
	base = f"orthography.{name}"
	repl_section = base + ".replacements"
	rules_section = base + ".rules"
	encl_section = base + ".enclosures"

	has_repl = cfg.has_section(repl_section)
	has_rules = cfg.has_section(rules_section)
	has_encl = cfg.has_section(encl_section)

	if not (has_repl or has_rules or has_encl):
		raise ValueError(
			f"Missing orthography profile definition for '{name}'. "
			f"Expected at least one of "
			f"[{repl_section}], [{rules_section}], or [{encl_section}]"
		)

	replacements = {}
	if has_repl:
		for k, v in cfg.items(repl_section):
			src = SPECIAL_REPLACEMENT_KEYS.get(k, k)
			replacements[src] = v

	rules = []
	if has_rules:
		for _, v in cfg.items(rules_section):
			if "=>" not in v:
				raise ValueError(
					f"Invalid orthography rule in [{rules_section}]: {v!r} "
					f"(expected: PATTERN => REPLACEMENT)"
				)
			pat_str, repl_str = map(str.strip, v.split("=>", 1))
			rules.append(NormRule(re.compile(pat_str), repl_str))

	enclosures = []
	if has_encl:
		for key, v in cfg.items(encl_section):
			if ";" not in v:
				raise ValueError(
					f"Invalid enclosure rule '{key}' in [{encl_section}]: {v!r} "
					f"(expected: SYMBOLS ; COMMAND)"
				)
			symbols_str, command = map(str.strip, v.split(";", 1))
			if not symbols_str:
				raise ValueError(
					f"Empty symbol list in enclosure rule '{key}' of [{encl_section}]"
				)
			if not command:
				raise ValueError(
					f"Empty command name in enclosure rule '{key}' of [{encl_section}]"
				)

			chars = "".join(dict.fromkeys(symbols_str))
			pat = re.compile("[" + re.escape(chars) + "]+")
			enclosures.append(EnclosureRule(pat=pat, command=command))

	return OrthoProfile(
		replacements=replacements,
		rules=rules,
		enclosures=enclosures
	)

