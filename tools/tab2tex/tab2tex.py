"""
tab2tex_v1.py

Convert RyuTab-formatted Excel data into TeX source for the RyuTeX template.

Outputs:
	\\entry{\\headPrint[...]}{\\MeaningsBlock{\\meaning{...}{...}{...}...}}{\\tailPrint[...]}
	(and optionally reverse-index entries via \\rentry)

Design principles:
- Source data (RyuTab) contains no TeX commands.
- All TeX is generated by this script.
- Escaping of TeX special characters is applied systematically to all input text.
- Ryukyuan orthographic normalization and ruby processing are handled explicitly by field type.

Text processing:
- Ryukyuan fields → escaped + orthography profile applied
- Japanese prose fields → escaped + embedded Ryukyuan (〈...〉) processed + ruby applied
- Ruby fields → escaped + ruby processing
- Plain Japanese fields → escaped only

Sorting and grouping principles:
- Entry sorting and headword grouping are not based on raw Unicode order.
- Sorting uses a hard-coded gojūon-based collation.
- Before sorting/grouping, kana strings are normalized in two layers:

  1. Core kana cleaning
	- remove whitespace
	- remove user-configurable ignorable characters
	- normalize small kana to full-size kana
	- remove voicing distinctions (e.g. は/ば/ぱ sort together)

  2. Script unification
	- convert katakana to hiragana for sorting/grouping only

- As a result, sorting/grouping intentionally conflates:
	- hiragana and katakana
	- small and full-size kana
	- voiced / semi-voiced and unvoiced kana

- Displayed headletters are derived separately from grouping keys:
	- in `auto` mode, core kana cleaning is applied but script is preserved
	- in `hiragana` mode, script is additionally unified to hiragana

- This means collation, grouping, and display are related but not identical operations.

Markup conventions in RyuTab:
- 〈...〉 : embedded Ryukyuan inside Japanese text
- ｜...〔...〕 / ...〔...〕 : ruby notation
- These are treated as structured markup, not literal characters

Notes:
- All key–value pairs are always braced: key={value}
  (ensures correct TeX parsing even when values contain commas)
- Validation of RyuTab format (including reserved characters and markup correctness)
  is expected to be performed separately.
- The current collation logic is intentionally hard-coded; more specialized section systems
  (e.g. custom handling of は/ば/ぱ or script-sensitive sectioning) would require a separate,
  explicitly configurable collation/section design.
"""

