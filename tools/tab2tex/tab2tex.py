@dataclass
class Settings:
	excel_path: Path = Path()
	sheet_main: str = "goi"
	sheet_example: Optional[str] = None
	profile: str = ""
	ignorable_for_sort: str = ""
	headletters: bool = True
	headletter_mode: str = "auto"

def load_settings(path: str) -> tuple[Settings, HeaderMap, OrthoProfile]:
	cfg = configparser.ConfigParser()
	cfg.optionxform = str # Case-sensitive

	loaded = cfg.read(path, encoding="utf-8")
	if not loaded:
		raise ValueError(f"Settings file not found or unreadable: {path}")

	# Initialize an instance of the Settings class
	s = Settings()

	# strings
	if not cfg.has_option("io", "excel_path"):
		raise ValueError("Missing required setting: [io] excel_path")

	excel_path_raw = cfg.get("io", "excel_path").strip()

	if excel_path_raw == "":
		raise ValueError("Setting [io] excel_path is empty")

	excel_path = Path(excel_path_raw)

	if not excel_path.is_file():
		raise ValueError(f"Excel file not found or not a file: {excel_path}")

	s.excel_path = excel_path
		
	s.sheet_main = cfg.get("io", "sheet_main", fallback=s.sheet_main)
	s.sheet_example = cfg.get("io", "sheet_example", fallback=None) # Optional example sentence sheet
	s.profile = cfg.get("orthography", "profile", fallback="").strip()
	s.ignorable_for_sort = cfg.get("sorting", "ignorable_chars", fallback="")

	# headletters
	s.headletters = cfg.getboolean("output", "headletters", fallback=True)
	s.headletter_mode = cfg.get("output", "headletter_mode", fallback="auto").strip().lower()
	if s.headletter_mode not in {"auto", "hiragana"}:
		raise ValueError(
			f"Invalid [output] headletter_mode: {s.headletter_mode!r} "
			f"(expected 'auto' or 'hiragana')"
		)

	# load header map
	hmap = load_header_map(cfg)
	profile = load_ortho_profile(cfg, s.profile)

	return s, hmap, profile

