def build_rei_index(df_rei: pd.DataFrame, hex: Dict[str, str]) -> Dict[str, List[Tuple[str, str]]]:
	require_cols_semantic(df_rei, hex, [EX_MID, EX_KANA, EX_JPTRSL], "rei")

	out: Dict[str, List[Tuple[str, str]]] = {}

	for _, row in df_rei.iterrows():
		fval = get_cell_semantic(row, hex, EX_FILTER)
		if fval != "":
			continue	

		mid = get_cell_semantic(row, hex, EX_MID)
		src = get_cell_semantic(row, hex, EX_KANA)
		jp = get_cell_semantic(row, hex, EX_JPTRSL)

		# Skip rows lacking MEANING_ID or KANA
		if mid == "" or src == "":
			continue

		out.setdefault(mid, []).append((src, jp))

	return out

